# ORG 0070H
# DB 3A,7F,B2,18,E4,C9,56,A7,F1,8D,02,99,6E,D3,40,BE,75,1C,AA,64
# ORG 0000
	   MVI C,14
	   LXI H,0070

ODD:	   MOV A,M
	   ANI 01
	   JZ EVEN
	   INR D	// ODD COUNTER
	   JMP UPDATE

EVEN:	   INR E           // EVEN COUNTER
	   JMP UPDATE

UPDATE:	   INX H
	   DCR C
	   JZ HOLD
	   JMP ODD

HOLD:	   MOV A,D
	   OUT 21	// ODD COUNTS
	   MOV A,E
	   OUT 20	//EVEN COUNTS
	   HLT
